// Исходные числовые переменные
let a = 10;
let b = 5;
let c = 3;

// 1. Сложите b и a (и сохраните результат в переменную, например, в `a`)
a += b; // Эквивалентно: a = a + b;

// 2. Разделите a на c (и сохраните результат в `a`)
a /= c; // Эквивалентно: a = a / c;

// 3. Замените значение модуляцией c и b (остаток от деления c на b)
c %= b; // Эквивалентно: c = c % b;

// 4. Выведите все три числа на экран
console.log(`a = ${a}, b = ${b}, c = ${c}`);
